# Sum of two floating point numbers 
float_1 = 3.9
float_2= 2.8
sum_result = float_1 + float_2
print("Data type:", type(sum_result))
print("Sum of two floating point numbers:", sum_result)
# Difference between two integers
int_1 = 8924
int_2 = 8857
difference_result = int_1 - int_2
